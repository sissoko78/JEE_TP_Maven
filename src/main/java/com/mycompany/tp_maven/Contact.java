package com.mycompany.tp_maven;

public class Contact {
    public String Nom;
    public String Prenom;
    public String Email;
    public String Comptence_favorie;

    public Contact(String Nom, String Prenom, String Email, String Comptence_favorie) {
        this.Nom = Nom;
        this.Prenom = Prenom;
        this.Email = Email;
        this.Comptence_favorie = Comptence_favorie;
    }
}
